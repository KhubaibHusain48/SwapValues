public class Swap {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c;
        System.out.println("Value of a is " + a + " and b is " + b);
        System.out.println("Swapping Completed.........");

        c = a;
        a = b;
        b = c;

        System.out.println("Value of a is " + a + " and b is " + b);

    }
}
